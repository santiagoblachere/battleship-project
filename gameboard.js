	place(ship, player, start, direction) {
		// start = [3,4]
		if (player === "playerA") {
			let y = start[0];
			let x = start[1];
			for (let index = 0; index < ship.length; index++) {
				if (this.playerA[y][x] === undefined || this.playerA[y][x] === 1)
					return "CANT PLACE THERE";
				this.playerA[y][x] = 1;
				if (direction == "left") {
					x = x - 1;
				}
				if (direction == "right") {
					x = x + 1;
				}
				if (direction == "up") {
					y = y - 1;
				}
				if (direction == "down") {
					y = y + 1;
				}
			}
		}
	}
